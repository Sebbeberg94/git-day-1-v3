public class Main {
    public static void main(String[] args) {
        System.out.println(returnInt(5));
        System.out.println(returnIntMult(3, 4));
    }
    public static int returnInt(int x) {
        return x + 1;
    }
    public static int returnIntMult(int x, int y) {
        return x * y;
    }
}
